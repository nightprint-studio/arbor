/**
 * Popular community themes packaged as importable presets.
 *
 * Each preset is a partial colour override applied on top of the built-in
 * Dark theme so non-colour tokens (shadows, scrollbar geometry, etc.) stay
 * consistent across presets. They are exposed as `built_in: false` Themes
 * with stable `preset-*` ids so the editor can show them in a dedicated
 * "Presets" gallery and the user can import them into their custom list
 * (one click clones with a fresh `custom-*` id, ready to apply / edit).
 */

import darkTheme from './dark.json';
import type { Theme } from '$lib/types/theme';

type Preset = {
  id:          string;
  name:        string;
  description: string;
  vars:        Record<string, string>;
};

const baseVars = (darkTheme as Theme).vars;

function makeTheme(p: Preset): Theme {
  return {
    id:          p.id,
    name:        p.name,
    description: p.description,
    built_in:    false,
    vars:        { ...baseVars, ...p.vars },
  };
}

